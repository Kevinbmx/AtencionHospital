    public void cargarTablaUsuario() {
        try {
            logger.info("Cargando tabla Ususario.");
            DefaultTableModel tabla = new DefaultTableModel();
            tabla.addColumn("ID");
            tabla.addColumn("NOMBRE");
            tabla.addColumn("APELLIDO");
            tabla.addColumn("USUARIO");
            tabla.addColumn("CONTRASEÃ‘A");
            cts = cn.prepareStatement("SELECT * FROM selectUsuario()");
            r = cts.executeQuery();
            while (r.next()) {
                Object dato[] = new Object[5];
                for (int i = 0; i < 5; i++) {
                    dato[i] = r.getString(i + 1);
                }
                tabla.addRow(dato);
            }
            this.jTable1.setModel(tabla);

            jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            jTable1.doLayout();
        } catch (Exception e) {
        }
    }

    public void cargarTablaPermiso() {
        try {
            logger.info("Cargando tabla Permiso.");
            DefaultTableModel tabla = new DefaultTableModel();
            tabla.addColumn("ID");
            tabla.addColumn("NOMBRE");
            cts = cn.prepareStatement("SELECT * FROM selectPermiso()");
            r = cts.executeQuery();
            while (r.next()) {
                Object dato[] = new Object[2];
                for (int i = 0; i < 2; i++) {
                    dato[i] = r.getString(i + 1);
                }
                tabla.addRow(dato);
            }
            this.jTable2.setModel(tabla);

            jTable2.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            jTable2.doLayout();
        } catch (Exception e) {
        }
    }

    private void insertar(String idU, String idP) throws SQLException {
        PreparedStatement cts = cn.prepareStatement("exec insertarPermisoUsuario ?, ?");
        cts.setString(1, idU);
        cts.setString(2, idP);
        int rpt = cts.executeUpdate();
        cts.getMoreResults();
        if (rpt == 1) {
            JOptionPane.showMessageDialog(this, "Se ha insertado");
            logger.info("Se ha insertrado. idUsuario: "+idU+" - idPermiso: "+idP);
            this.dispose();
        }
    }

